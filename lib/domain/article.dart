import 'package:equatable/equatable.dart';

class Article extends Equatable {
  String? author;
  String? title;
  String? description;
  String? url;
  String? urlToImage;
  String? publishedAt;
  String? content;

  Article(
      {this.author,
      this.title,
      this.description,
      this.url,
      this.urlToImage,
      this.publishedAt,
      this.content});

  @override
  List<Object?> get props =>
      [author, title, description, url, urlToImage, publishedAt, content];
}
